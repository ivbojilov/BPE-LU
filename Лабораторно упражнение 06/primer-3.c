/*
Пример 3: Програма, която увеличава с две стойността на едно въведено от потребителя цяло
число с помощта на потребителска функция, която използва указател като параметър.
*/

#include <stdio.h>

void change(int *a); // параметърът е указател

int main()
{
	int x;
	printf("x = "); scanf("%d", &x);
	change(&x);
	printf("change = %d\n", x);

	return 0;
}

void change(int *a)
{
	*a = *a + 2;
}
