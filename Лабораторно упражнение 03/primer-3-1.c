/*
Пример 3.1: Програма, която отпечатва стойностите от 0 до 5, като прескача 3. В нея се използва
цикъл с предусловие и оператор continue или цикъл for.
*/

#include <stdio.h>
int main()
{
	int i;
	
	for ( i = 0; i < 6; i++)
	{
		if (i == 3)
		{
			continue;
		}
		printf("%d ", i);
	}
	return 0;
}	



