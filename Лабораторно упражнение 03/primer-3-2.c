/*
Пример 3.2: Програма, която отпечатва стойностите от 0 до 5, като прескача 3. В нея се използва
цикъл с предусловие и оператор continue или цикъл for.
*/

#include <stdio.h>

int main()
{
	int i = 0;
	
	while(i < 6)
	{
		if (i == 3)
		{
			i++;
			continue;
		}
		printf("%d ", i);
		i++;
	}
	
	return 0;
}
